public class EscapeSequence {
  public static void main() {

      System.out.println("hello yumna\b welocome");//backspace
      System.out.println("hello yusra\r welcome");//carraige return
      System.out.println("hello \'yumna\'");//single quote
      System.out.println("hello \"yumna\"");//double quote
      System.out.println("hello\tyumna");//4 spaces (tab)
      System.out.println("hello\nyumna");//newline
      System.out.println("hello\\yumna\\");//backlash
    }
}
